package com.uagrm.informatica.johana.radar;

class Ubicacion {
    String idUser;
    String latitud;
    String longitud;
    String orden;

    public Ubicacion(String idUser, String latitud, String longitud, String orden) {
        this.idUser = idUser;
        this.latitud = latitud;
        this.longitud = longitud;
        this.orden = orden;
    }
}
